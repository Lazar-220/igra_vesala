    public void pogadjanje(Slovo s) {
        if(s.getPozicije()!=null&&!s.getPozicije().isEmpty()){
            for(int i=0;i<s.getPozicije().size();i++){
                switch (s.getPozicije().get(i)) {
                    case 1:
                        jTextFieldSlovo1.setText(s.getKarakter()+"");
                        break;
                    case 2:
                        jTextFieldSlovo2.setText(s.getKarakter()+"");
                        break;
                    case 3:
                        jTextFieldSlovo3.setText(s.getKarakter()+"");
                        break;
                    case 4:
                        jTextFieldSlovo4.setText(s.getKarakter()+"");
                        break;
                    case 5:
                        jTextFieldSlovo5.setText(s.getKarakter()+"");
                        break;
                        
                    default:
                        throw new AssertionError();
                }
            }
        }
    }

