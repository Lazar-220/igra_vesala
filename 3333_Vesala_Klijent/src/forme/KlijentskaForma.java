    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        jLabelRb.setText(rb+". igrac");
        if( !jTextFieldSlovo1.getText().isEmpty()&&
                !jTextFieldSlovo2.getText().isEmpty()&&
                !jTextFieldSlovo3.getText().isEmpty()&&
                !jTextFieldSlovo4.getText().isEmpty()&&
                !jTextFieldSlovo5.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bravo, pogodio si rec! Tvoj protivnik ce uskoro zavrsiti sa pogadjanjem pa ces saznati ko je pobedio!");
            jButton1.setEnabled(false);
            return;
        }
        Slovo s=new Slovo(Character.toUpperCase(jTextFieldSlovoPokusaj.getText().charAt(0)), new ArrayList<>(),rb);
        if(pokusanaSlova.contains(Character.toUpperCase(jTextFieldSlovoPokusaj.getText().charAt(0))+"")){
            JOptionPane.showMessageDialog(this, "Bilo je to slovo!");
            return;
        }
        pokusanaSlova+=s.getKarakter();
        pokusanaSlova+=", ";
        System.out.println(rb);
        KlijentskiZahtev kz=new KlijentskiZahtev(operacije.Operacije.pogadjanje, (Slovo)s);
        komunikacija.Komunikacija.getInstance().posaljiZahtev(kz);
        
        
        jTextFieldSlovoPokusaj.setText("");
        preostaliBrojPokusaja--;
        if(preostaliBrojPokusaja<=0){
            JOptionPane.showMessageDialog(this, "Istrosio si sve pokusaje!");
            jButton1.setEnabled(false);
        }
        jTextFieldPreostaliBrojPokusaja.setText(preostaliBrojPokusaja+"");
        jTextFieldKoriscenaSlova.setText(pokusanaSlova);
       
    }//GEN-LAST:event_jButton1ActionPerformed

